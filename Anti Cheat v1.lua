function getClosestPlayer()
	local closestPlayer = nil
	local shortestDistance = math.huge

	for i, v in pairs(game:GetService("Players"):GetPlayers()) do
		if v ~= localPlayer and v.Character and v.Character:FindFirstChild("Humanoid") and v.Character.Humanoid.Health ~= 0 and v.Character:FindFirstChild("HumanoidRootPart") and v.Character:FindFirstChild("Head") then
			local magnitude = (v.Character.HumanoidRootPart.Position - localPlayer.Character.HumanoidRootPart.Position).magnitude

			if magnitude < shortestDistance then
				closestPlayer = v
				shortestDistance = magnitude
			end
		end
	end

	return closestPlayer
end

function ClosestPlayerToMouse()
	local target = nil
	local dist = math.huge
	for i,v in pairs(players:GetPlayers()) do
		if v.Name ~= plr.Name then
			if v.Character and v.Character:FindFirstChild("Humanoid") and v.Character.Humanoid.Health ~= 0 and v.Character:FindFirstChild("HumanoidRootPart") then
				local screenpoint = camera:WorldToScreenPoint(v.Character.HumanoidRootPart.Position)
				local check = (Vector2.new(mouse.X,mouse.Y)-Vector2.new(screenpoint.X,screenpoint.Y)).magnitude

				if check < dist then
					target  = v
					dist = check
				end
			end
		end
	end

	return target 
end

function notBehindWall(target)
	local ray = Ray.new(plr.Character.Head.Position, (target.Position - plr.Character.Head.Position).Unit * 300)
	local part, position = game:GetService("Workspace"):FindPartOnRayWithIgnoreList(ray, {plr.Character}, false, true)
	if part then
		local humanoid = part.Parent:FindFirstChildOfClass("Humanoid")
		if not humanoid then
			humanoid = part.Parent.Parent:FindFirstChildOfClass("Humanoid")
		end
		if humanoid and target and humanoid.Parent == target.Parent then
			local pos, visible = camera:WorldToScreenPoint(target.Position)
			if visible then
				return true
			end
		end
	end
end



